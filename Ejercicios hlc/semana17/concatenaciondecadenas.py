'''Concatenacion de cadenas: Une dos strings ingresados por el usuario'''
def concatenar_cadenas():
    cadena1 = input("Ingrese la primera cadena: ")
    cadena2 = input("Ingrese la segunda cadena: ")
    print("La concatenacion de las cadenas es: ", cadena1 + cadena2)
    return
#Ejemplo de uso de la funcion concatenar_cadenas()
concatenar_cadenas()
