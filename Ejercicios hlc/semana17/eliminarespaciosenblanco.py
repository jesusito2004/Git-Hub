'''Eliminacion de espacios en blancos: Elimina los espacios del inicio y al final de su cadena'''
s = "   hola mundo   "
print(s.strip())  # hola mundo
print(s.lstrip())  # hola mundo   (elimina espacios al inicio)
print(s.rstrip())  #   hola mundo (elimina espacios al final)
print("Jesús Sánchez Rodríguez")
